package testOutput.runner;

public class Info {
	private String strCmd;
	private String host;
    private String formName;
    private String password;
    private String replayAddress;
    private String subject;
    private String toAddress;
    private String content;
    private String [] fileName;

	public String getStrCmd() {
		return strCmd;
	}
	public void setStrCmd(String strCmd) {
		this.strCmd = strCmd;
	}
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getFormName() {
		return formName;
	}
	public void setFormName(String formName) {
		this.formName = formName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getReplayAddress() {
		return replayAddress;
	}
	public void setReplayAddress(String replayAddress) {
		this.replayAddress = replayAddress;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getToAddress() {
		return toAddress;
	}
	public void setToAddress(String toAddress) {
		this.toAddress = toAddress;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String[] getFileName() {
		return fileName;
	}
	public void setFileName(String[] fileName) {
		this.fileName = fileName;
	}
    
    

}
